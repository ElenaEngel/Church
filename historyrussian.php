                    <h2>Освящение нового храма в Западно-Американской епархии</h2>
                       <p>
                    <cite>Дьякон Павел Дроздовский, Сакраменто, статья взята с Wadiocese.org. </cite><br>

                    В воскресенье 4 мая, совершилось Великое освящение храма в честь
                    Святого Великомученика Георгия Победоносца в г. Солт-Лейк-Сити, шт. Юта.
                    Освящение было совершено Его Высокопреосвященством Архиепископом Кириллом Сан-Францисским
                    и Запано-Американским. А ему сослужили Его Преосвященство Епископ Сиэтлийский Феодосий,
                    Архимандрит Ириней (церковь св. Тихона Задонского в г. Сан-Франциско),
                    протоиерей Алексей Котар (Николаевский собор в г. Сиэтле),
                    настоятель храма иерей Михаил ван Опстал, иерей Иессей Файло (Миссия Св.
                    Иоанна Шанхайского в г. Кенневик, шт. Вашингтон), а также иерей Юстин Хавенс (Антиохийской юрисдикции).
                    Из дьяконского чина сослужили протодьякон Павел Билибин (Николаевский собор в г. Сиэтле),
                    дьякон Павел Дроздовский (Свято-Вознесенская церковь в г. Сакраменто) и дьякон Андрей Гарин
                    (Собор в честь иконы Божьей Матери «Всех скорбящих радость» в г. Сан-Франциско).
                    На это событие был приглашен талантливый регент дьякон Александр Петровский
                    (церковь Св. Георгия в г. Цинциннати, шт. Огайо) под коим руководством хор пел великолепно.
                    Во время Литургии за малым ходом отец Михаил был награжден правом ношения золотого наперстного креста,
                    и сразу возведен в сан протоиерея. Храм был переполнен молящимися, и почти все причастились
                    Святых Христовых Тайн. По окончании Литургии прихожанин Кевин Ботт был награжден Орденом Св.
                    Иоанна Шанхайского Второй степени. Ему также подарили икону его Небесного покровителя.
                    За тем изобильный обед был устроен в местном зале.
                    Тогда Архиепископ Кирилл объяснил значение вклада господина Ботт для Церкви.</p>

 <p>Больше 25 лет тому назад существовала миссия Русской Православной Церкви Заграницей
     в г. Солт-Лейк-Сити, который известен как центр Церкви Иисуса Христа Святых последних дней.
     Эта миссия – посвященная
     Св. Николаю чудотворцу – долго не просуществовала по отсутствию постоянного священного лица.
     Однако, в 2005 году новыми согласованными усилиями была создана миссия в честь
     святого великомученика Георгия, которая стала расти. Успехи стали ощутимыми после
     рукоположения в священники Михаила ван Опстал, члена прихода, который преподавал математику
     в университете штата Юта. Отец Михаил также является выпускником Пастырской школы Чикагской епархии.
                    </p>
                    <p>Маленькая община быстро умножалась (сегодня 150 членов), а временное помещение
                        оказалось недостаточным. В 2009 году членам миссии удалось купить подходящий
                        участок под настоящий храм. Около этого времени прибыла «Курская» икона Божьей Матери и
                        отец Михаил с прихожанами освятил новый участок Ею.
                        </p>
                        <p>Позже на том месте Архиепископ Кирилл совершил «Чин закладки храма» 5 мая, 2012 г.
                            Постройка начилась в июне 2013 года строительной компанией Палсон Констракшэн.</p>
                        <p>Поручили архитектору (фирма MJSA) создать традиционную Русскую церковь в
                            простом и трезвом стиле. Новгородское зодчество стало вдохновением для дизайна и в храме св.
                            Георгия есть признаки церквей Юрьева и Антоньева монастырей того края.
                            Образец строгих, черных куполов был взят из Антоньева монастыря.
                            Генеральный план включает в себя приходской зал и стоянку для автомобилей.</p>
                        <p>Многие важные события в истории прихода были связаны с праздниками
                            Божьей Матери. Не удивительно, что постройка нового
                            храма была закончена на праздник Похвалы Пресвятой Богородицы:
                            новый деревянный пол был покрыт лаком, новая паникадила была повешена и аналои сооружены.</p>
                        <p>
Слава Богу, что самый новый храм в епархии и единственный в честь Святого Славного, Великомученика, Победоносца
и чудотворца Георгия сможет служить светилом Православия для многих поколений. Подай, Господи!<br></p>


