			<h2>History</h2>
                        <p class ="sixth"><b>July 4, 2004:</b><br>  Michael van Opstall and family move to Salt Lake City for a new job, with instructions from northwest dean (благочиний северо-западного округа) archpriest Alexei Kotar to investigate starting a mission of the Russian Orthodox Church.</p>

 <p class ="sixth"><b>May 14, 2005:</b><br> Fr. Alexei Kotar serves the first Liturgy for the new St. George Mission in a rented hall. Subsequent services in this space were conducted by Fr. Alexei and Fr. David Moser.</p>

 <p class ="sixth"><b>October 2005:</b> <br> A former Greek church building comes available for rent, complete with iconostasis, altar table, and icons. An akathist is served on Pokrov to initiate the new building, and Fr. Alexei returns on October 22 to serve the first liturgy in this new location.</p>

 <p class ="sixth"><b>January 7, 2006:</b><br>  The parish’s first Nativity service was served by archimandrite Anastassy Zagarsky.
Weekly reader’s services (обедница) with akathist to St. George continue, with Liturgy once a month, served variously by Fr. Alexei, Fr. David, Fr. Sergei Kotar, and Fr. Peter Perekrestov.</p>

 <p class ="sixth"><b>May 6, 2006:</b> <br> The parish’s first parish holiday is celebrated by Fr. Alexei Kotar together with Fr. Blasko Paraklis (SOC).</p>

 <p class ="sixth"><b>November 19, 2006:</b><br>  The parish has its first hierarchal visit by archbishop Kyrill of San Francisco, together with future bishop (then archimandrite) Theodosy. Vladyka Kyrill ordains Michael van Opstall to the diaconate.</p>

 <p class ="sixth"><b>December 19, 2006:</b><br>  Deacon Michael is ordained priest by archbishop Kyrill at St. Nicholas Cathedral in Seattle, WA.</p>

 <p class ="sixth"><b>August 2009:</b><br>  The parish purchases one acre of land in West Jordan, UT.</p>

 <p class ="sixth"><b>October 18, 2009:</b> <br> The parish is visited for the first time by the Kursk Root icon of the Theotokos, and Fr. Alexei and Fr. Michael perform the blessing of the land for the future church.</p>

 <p class ="sixth"><b>May 5, 2012:</b><br> Archbishop Kyrill places the dedication stone for the new church.</p>

 <p class ="sixth"><b>May 30, 2013:</b> <br> Construction begins on the new church.</b></p>

 <p class ="sixth"><b>Summer and Fall 2013:</b> <br> Construction continues, with dome raising December 6, 2013.</p>

 <p class ="sixth"><b>May 4, 2014:</b><br>  The Great Consecration of St. George Russian Orthodox Church is performed by archbishop Kyrill.



	
			
