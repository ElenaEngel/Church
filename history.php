                    <h2>Consecration of a New Church in the Western American Diocese</h2>
                       <p>
                    <cite>by Deacon Paul Drozdowski, Sacramento, Retrieved from Wadiocese.org. </cite><br>
                    
                    On Sunday, May 4th, the Great Consecration took place of the new temple of the parish 
                    of St. George in Salt Lake City, Utah. The consecration was presided by His Eminence, 
                    Archbishop Kyrill of San Francisco. Concelebrating with him were His Grace, 
                    Bishop Theodosy of Seattle, Archimandrite Irenei (St. Tikhon of Zadonsk Church, San Francisco), 
                    Archpriest Alexey Kotar, Dean (St. Nicholas Cathedral, Seattle), Priest Michael van Opstall,
                    parish rector, Priest Jesse Philo (St. John the Wonderworker Church, Kennewick, WA), 
                    and Fr. Justin Havens (Antiochian Archdiocese). They were also joined by Protodeacon Paul Bilibin
                    (St. Nicholas Cathedral, Seattle), deacon Paul Drozdowski (Holy Ascension Church, Sacramento),
                    and deacon Andrei Garine (Holy Virgin Cathedral, San Francisco). 
                    For the occasion of the consecration, the choir sang beautifully under the talented direction 
                    of Deacon Alexander Petrovsky of St. George's Church, Cincinnati, Ohio. 
                    During the Small Entrance, Father Michael van Opstall was awarded the gold cross
                    and also elevated to the rank of archpriest. The church was filled to capacity, 
                    and nearly all partook of the Holy Mysteries. At the conclusion of the Liturgy, 
                    parishioner Kevin Bott was awarded the Order of St. John, second class, 
                    for his zeal in contributing to the construction of the temple; he was also presented with a
                    hand painted icon of his patron saint. Afterwards, a magnificent banquet was held in a local venue, 
                    where His Eminence explained the significance of Kevin's contribution to the Church.</p>

 <p>Over 25 years ago, there was once a mission belonging to Russian Orthodox Church Outside 
     of Russia in Salt Lake City, a city famous for being the headquarters of the Mormon church. This mission, dedicated to St. Nicholas, was short lived, due to the lack of clergy. However, in 2005, another concerted effort at establishing a mission, this time dedicated to St. George, was met with greater success. This success became tangible with the ordination of Michael van Opstall, a member of the mission and mathematics professor at University of Utah, to the priesthood in 2006. Fr. Michael is also a graduate of the Pastoral School of the Middle American Diocese.
                    </p>
                    <p>As the small congregation steadily grew (it now numbers 150), 
                        the temporary chapel soon proved inadequate. 
                        In 2009, the community was able to purchase an appropriate parcel 
                        of land to build a proper church building. Once, when the "Kursk-Root" Icon of the Theotokos visited, Fr. Michael and the faithful took the opportunity to bless this newly acquired property.
                        </p>
                        <p>Some time later, “The Rite of the Founding of a Temple” was performed by Archbishop Kyrill 
                            on May 5, 2012, and construction began in June 2013 by Paulsen Construction.</p>
                        <p>The architectural firm MJSA was hired to design a traditional Russian style church with simplicity and sobriety. The style of the church was inspired by churches of northwest Russia, loosely based on the Yuriev and the Antoniev monasteries in Novgorod. The monastic style black domes were particularly inspired by the latter. The construction project also included a community hall and proper parking lot.</p>
                        <p>Many happenings at St. George's church having to do with property are tied to holidays of the Theotokos, so it is not surprising that the interior of the church was finished enough in time for the Laudation of the Theotokos. The hardwood floor was stained & finished, chandeliers hung, analoys constructed. The iconostasis frame has temporary icons which are being gradually replaced by handpainted ones.</p>
                        <p>Glory to God, this newest church in our diocese, and the only one dedicated to the Glorious Great-martyr, Trophy-bearer and Wonderworker George, will serve as a bastion of Orthodoxy for generations as the parish continues to flourish. May God so grant!<br></p>
              

